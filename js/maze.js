const TILE_SIZE = 26;
const COLS = 21;
const ROWS = 23;

const TILE = {
  WALL: 1,
  PELLET: 0,
  POWER: 2,
  EMPTY: 3,
  DOOR: 4,
  HOUSE: 5,
};

function parseMaze(layoutIdx = 0) {
  const layout = MAZE_LAYOUTS[layoutIdx % MAZE_LAYOUTS.length];
  const grid = [];
  for (let r = 0; r < ROWS; r++) {
    const row = [];
    for (let c = 0; c < COLS; c++) {
      const ch = layout[r][c];
      row.push(parseInt(ch, 10));
    }
    grid.push(row);
  }
  return grid;
}

function countPellets(grid) {
  let n = 0;
  for (let r = 0; r < ROWS; r++) {
    for (let c = 0; c < COLS; c++) {
      if (grid[r][c] === TILE.PELLET || grid[r][c] === TILE.POWER) n++;
    }
  }
  return n;
}

function isWalkable(tile) {
  return tile !== TILE.WALL && tile !== undefined;
}

